use std::io::Write;

fn main() {
    let xor_mask = b"\x93\xcf\x8a\x96\xa5\xd2\x9c\xb0\xd2\x8b\x86\xd2\xad\xb0\x9d\xb6\x8c\xa5\xd2\x95\xbf\xd2\x85\xd2\x8b\xcf\x9d\x90\x92\xd2\xb0\x8c\xa5\x9e\xb6\x9a\x93\x9a\xba\x9a\xba\x9a\xba";
    let key: Vec<u8> = xor_mask.iter().map(|m| m ^ 0xFF).collect();
    std::io::stdout().write_all(&key).unwrap();
    println!();
}
